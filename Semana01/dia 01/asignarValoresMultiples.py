#Muchos valores para multiples variables
x,y,z = "Orange","Banana","Cherry"
print(x)
print(y)
print(z)

a,b,c = "1","2","3"
print(a)
print(b)
print(c)

#Un valor para múltiples variables
q = w = e = "Green"
print(q)
print(w)
print(e)

r = t = y = "Blue"
print(r)
print(t)
print(y)

#Desempaquetar una colección
coleccion = [12,23,32]
c,v,b = coleccion
print(c)
print(v)
print(b)

coleccion2 = ["modelo1","modelo2","modelo3"]
g,h,j = coleccion2
print(g)
print(h)
print(j)
